from django import http
from django.template import RequestContext, loader

from merengue.middleware import get_context


def page_not_found(request, template_name='404.html'):
    """
    Default 404 handler.

    Templates: `404.html`
    Context:
        request_path
            The path of the requested URL (e.g., '/app/pages/bad_page/')
    """
    t = loader.get_template(template_name) # You need to create a 404.html template.
    extra_context = get_context(request)
    extra_context.update({'request_path': request.path})
    return http.HttpResponseNotFound(t.render(RequestContext(request, extra_context)))
